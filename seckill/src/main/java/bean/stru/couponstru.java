package bean.stru;

public class couponstru {
    private String username = null;
    private int full = 0;
    private int cut = 0;

    public void setUsername(String username) {
        this.username = username;
    }

    public void setFull(int full) {
        this.full = full;
    }

    public void setCut(int cut) {
        this.cut = cut;
    }

    public String getUsername() {
        return username;
    }

    public int getFull() {
        return full;
    }

    public int getCut() {
        return cut;
    }
}
